# FLOATS
# Ejemplos básicos con floats
print("Valores floats")
print(192.3)
print(42.0021)
print(671.102)
print(-192.01)

# Operaciones
print("Resultado de operaciones con floats")
print("54*12.32 =", 54*12.32)
print("65+123*0.612-(23.05**2) =", 65+123*0.612-(23.05**2))
print("89.653/2.43 = ", 89.653/2.43)

# STRINGS
# Ejemplos básicos con Strings
print("Strings")
print("Hello, World!")
print('Python! Woooo!')
print("This is a basic Python example.")
print('She said, "Hello!"')

# Operaciones
print("Operaciones con Strings")
print("Hello, " + "World!")               # Concatenación
print("Python " * 3)                      # Repetición
print("Hello!"[0])                        # Indexación - imprimirá 'H'
print("World"[-1])                        # Indexación - imprimirá 'd'
print("Python".upper())                   # Convertir a mayúsculas
print("PYTHON".lower())                   # Convertir a minúsculas

# BOOLEANS
# Ejemplos básicos de Booleans
print(True)
print(False)

# Ejemplos básicos (y generales) con Booleans
print("True and False = ", True and False)  # AND
print("True or False = ", True or False)    # OR
print("not True", not True)                 # NOT
print("5 > 3", 5 > 3)                       # Greater than
print("5 < 3", 5 < 3)                       # Less than
print("5 == 5", 5 == 5)                     # Equality
print("5 != 5", 5 != 5)                     # Inequality

